public class Position {

    //variables for the x-axis and y-axis
    private int x;
    private int y;

    //getters and setters for the x-axis and y-axis
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
